package 上半.day6;

public class 方法_01_02_什么是方法_定义与调用方法 {
    public static void main(String[] args) {
        //调用方法
        playGame();
    }

    //定义一个方法
    public static void playGame() {
        System.out.println("吃饭");
        System.out.println("睡觉");
        System.out.println("打豆豆");
    }
}
